function openNewWindow(url) {
    window.open(url);
}

function hasOwnProperty(object, property) {
    return object.hasOwnProperty(property);
}

function removeProperty(object, property) {
    delete object.property;
    return null;
}

// Explicitly send pageview generated by URL-changing AJAX call to analytics server
// --------------------------------------------------------------------------------
function sendAnalyticsPageView(url) {
    if (window.plausible) {
        window.plausible('pageview', { props: {
            'page': url,
            'title': document.title
            }
        });
    }
}

function getCurrentTimeMillis() {
    var d = new Date();
    return d.getTime();
}

function pushState(tabId) {
    var stateObj = { "tabId": tabId };
    history.pushState(stateObj, null, null);
}
